<?php

namespace App\Services;

use Illuminate\Support\Arr;

class MotivationService
{
    /**
     * Контекстная мотивация под состояние пользователя.
     */
    public function forUser(int $streak, int $todayDone, int $todayTotal): string
    {
        // Идеальный день
        if ($todayTotal > 0 && $todayDone === $todayTotal) {
            return Arr::random([
                'Идеальный день. Так держать.',
                'Все привычки закрыты — ты на коне.',
                'Чисто. Ровно. На максимум.',
            ]);
        }

        // Большой стрик
        if ($streak >= 30) {
            return "Месяц без пропусков. Это уже стиль жизни.";
        }
        if ($streak >= 14) {
            return "{$streak} дней подряд — две недели стабильности.";
        }
        if ($streak >= 7) {
            return "Огонь не гаснет {$streak} дней. Не дай ему потухнуть.";
        }
        if ($streak >= 3) {
            return "{$streak} дня подряд. Скоро привычка закрепится.";
        }
        if ($streak === 1) {
            return 'Первый день. Главное — начать.';
        }

        // Стрик 0 + есть привычки
        if ($todayTotal > 0) {
            return Arr::random([
                'Один шаг сегодня — большой результат завтра.',
                'Маленькие действия каждый день меняют всё.',
                'Не нужно идеально — нужно регулярно.',
                'Лучшее время начать — прямо сейчас.',
                'Успех — это сумма мелких усилий, повторённых ежедневно.',
            ]);
        }

        // Совсем новый пользователь
        return Arr::random([
            'Добавь свою первую привычку — путь начинается с шага.',
            'Готов начать? Выбери пресет или создай свою привычку.',
            'Привычки решают всё. Начни с одной.',
        ]);
    }
}
